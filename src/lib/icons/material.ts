export function getMaterialIcon(
  name: string,
  isDir: boolean,
  expanded = false
): string {
  if (isDir) {
    const folderKey = name.toLowerCase();
    const folderIcon = FOLDER_NAME_MAP[folderKey];
    if (folderIcon) {
      return expanded ? `${folderIcon}-open` : folderIcon;
    }
    return expanded ? "folder-open" : "folder";
  }

  // Try exact filename match first
  const lowerName = name.toLowerCase();
  const fileNameIcon = FILE_NAME_MAP[lowerName];
  if (fileNameIcon) return fileNameIcon;

  // Try extension match
  const ext = lowerName.split(".").pop() ?? "";
  const extIcon = FILE_EXTENSION_MAP[ext];
  if (extIcon) return extIcon;

  return "file";
}

export function getMaterialIconUrl(name: string, isDir: boolean, expanded = false): string {
  const iconName = getMaterialIcon(name, isDir, expanded);
  return `/icons/material/${iconName}.svg`;
}
